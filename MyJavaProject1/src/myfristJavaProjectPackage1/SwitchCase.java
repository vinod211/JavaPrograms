package myfristJavaProjectPackage1;

public class SwitchCase {
	

	public static void main(String[] args) {
		int b = 3, v = 5 ,result;

	int excute = 0;

		switch (excute) {
		
		case 0:
			result = b + v;
			System.out.println(result);
		
		case 2:
			result = b - v;
			System.out.println(result);
			
		case 3:
			result=b*v;
			System.out.println(result);
		
		}
	}
}
